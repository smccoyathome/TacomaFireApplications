
namespace UpgradeHelpers.Helpers
{
    public enum FlatStyle
    {
        // Summary:
        //     The control appears flat.
        Flat = 0,
        //
        // Summary:
        //     A control appears flat until the mouse pointer moves over it, at which point
        //     it appears three-dimensional.
        Popup = 1,
        //
        // Summary:
        //     The control appears three-dimensional.
        Standard = 2,
        //
        // Summary:
        //     The appearance of the control is determined by the user's operating system.
        System = 3,
    }
}
